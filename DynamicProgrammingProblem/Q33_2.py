n=10
time=[5,4,3,2,1,1,2,3,4,5]
money=[50,40,30,20,10,10,20,30,40,50]
d=[0]*(n+1)
max_value=0
if time[n-1]==1:
    d[n-1]=money[n-1]
for i in range(n-2,-1,-1):
    if i+time[i]<=n:
        d[i]=max(max_value,money[i]+d[i+time[i]])
        max_value=d[i]
print(d)
print(max_value)

